//============================================================================
// 初期設定
//============================================================================
HRESULT CTutorial::Init()
{
	// 基底クラスの初期設定
	HRESULT hr{ CScene::Init() };

	// 背景生成
	CBg::Create();

	// 操作説明画面生成
	CExplanation::Create();

	return hr;
}

//============================================================================
// 終了処理
//============================================================================
void CTutorial::Uninit()
{
	// 基底クラスの終了処理
	CScene::Uninit();
}

//============================================================================
// 更新処理
//============================================================================
void CTutorial::Update()
{
	// 基底クラスの更新処理
	CScene::Update();

	// ゲーム画面へ
	if (CManager::GetKeyboard()->GetTrigger(DIK_RETURN))
	{
		CFade::SetFade(CScene::MODE::GAME);
	}
}

