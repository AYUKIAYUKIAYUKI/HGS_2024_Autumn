#include "bg.h"//背景ヘッダーファイル

//============================================================================
// コンストラクタ
//============================================================================
CBg::CBg(int nPriority) : CObject_2D{ nPriority }
{
}

//============================================================================
// デストラクタ
//============================================================================
CBg::~CBg()
{
}

//============================================================================
// 初期設定
//============================================================================
HRESULT CBg::Init()
{
	// テクスチャ設定
	BindTex(CTexture_Manager::TYPE::BG);

	// 2Dオブジェクト初期設定
	if (FAILED(CObject_2D::Init()))
	{
		return E_FAIL;
	}

	return S_OK;
}

//============================================================================
// 終了処理
//============================================================================
void CBg::Uninit()
{
	// 2Dオブジェクト終了処理
	CObject_2D::Uninit();
}

//============================================================================
// 更新処理
//============================================================================
void CBg::Update()
{
	// 2Dオブジェクト更新処理
	CObject_2D::Update();
}

//============================================================================
// 描画処理
//============================================================================
void CBg::Draw()
{
	// 2Dオブジェクト描画処理
	CObject_2D::Draw();
}

//============================================================================
// 生成処理
//============================================================================
CBg* CBg::Create()
{
	// メモリを動的確保
	CBg* pBg = new CBg();

	//パラメータ設定
	pBg->SetSize(CREATE_SIZE);// サイズ
	pBg->SetPos(CREATE_POS);// 座標
	pBg->SetTexWidth(5.0f);// 横のテクスチャ分割数
	pBg->SetTexHeight(5.0f);// 横のテクスチャ分割数

	//初期化処理
	pBg->Init();

	return pBg;
}
