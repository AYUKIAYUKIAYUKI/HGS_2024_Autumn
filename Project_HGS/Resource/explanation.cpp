#include "explanation.h"// 操作説明ヘッダーファイル

//============================================================================
// コンストラクタ
//============================================================================
CExplanation::CExplanation(int nPriority) : CObject_2D{ nPriority }
{
}

//============================================================================
// デストラクタ
//============================================================================
CExplanation::~CExplanation()
{
}

//============================================================================
// 初期設定
//============================================================================
HRESULT CExplanation::Init()
{
	// テクスチャ設定
	BindTex(CTexture_Manager::TYPE::EXPLANATION);

	// 2Dオブジェクト初期設定
	if (FAILED(CObject_2D::Init()))
	{
		return E_FAIL;
	}

	return S_OK;
}

//============================================================================
// 終了処理
//============================================================================
void CExplanation::Uninit()
{
	// 2Dオブジェクト終了処理
	CObject_2D::Uninit();
}

//============================================================================
// 更新処理
//============================================================================
void CExplanation::Update()
{
	// 2Dオブジェクト更新処理
	CObject_2D::Update();
}

//============================================================================
// 描画処理
//============================================================================
void CExplanation::Draw()
{
	// 2Dオブジェクト描画処理
	CObject_2D::Draw();
}

//============================================================================
// 生成処理
//============================================================================
CExplanation* CExplanation::Create()
{
	// メモリを動的確保
	CExplanation* pExplanation = new CExplanation();

	//パラメータ設定
	pExplanation->SetSize(CREATE_SIZE);// サイズ
	pExplanation->SetPos(CREATE_POS);// 座標
	
	//初期化処理
	pExplanation->Init();

	return pExplanation;
}
