#include "logo.h"// タイトルロゴヘッダーファイル

//============================================================================
// コンストラクタ
//============================================================================
CLogo::CLogo(int nPriority) : CObject_2D{ nPriority }
{
}

//============================================================================
// デストラクタ
//============================================================================
CLogo::~CLogo()
{
}

//============================================================================
// 初期設定
//============================================================================
HRESULT CLogo::Init()
{
	// テクスチャ設定
	BindTex(CTexture_Manager::TYPE::LOGO);

	// 2Dオブジェクト初期設定
	if (FAILED(CObject_2D::Init()))
	{
		return E_FAIL;
	}

	return S_OK;
}

//============================================================================
// 終了処理
//============================================================================
void CLogo::Uninit()
{
	// 2Dオブジェクト終了処理
	CObject_2D::Uninit();
}

//============================================================================
// 更新処理
//============================================================================
void CLogo::Update()
{
	// 2Dオブジェクト更新処理
	CObject_2D::Update();
}

//============================================================================
// 描画処理
//============================================================================
void CLogo::Draw()
{
	// 2Dオブジェクト描画処理
	CObject_2D::Draw();
}

//============================================================================
// 生成処理
//============================================================================
CLogo* CLogo::Create()
{
	// メモリを動的確保
	CLogo* pLogo = new CLogo();

	// パラメータ設定
	pLogo->SetSize(CREATE_SIZE);// サイズ
	pLogo->SetPos(CREATE_POS);// 座標

	// 初期化処理
	pLogo->Init();

	return pLogo;
}
