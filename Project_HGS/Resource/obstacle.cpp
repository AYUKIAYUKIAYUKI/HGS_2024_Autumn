//============================================================================
// 削除チェック
//============================================================================
void CObstacle::DeleteCheck()
{
	D3DXVECTOR3 Pos{ GetPos() }, Size{ GetSize() };

	if (Pos.x + Size.x < SCREEN_WIDTH * -1.0f || Pos.x - Size.x > SCREEN_WIDTH * 2.0f ||
		Pos.y + Size.y < SCREEN_HEIGHT * -1.0f || Pos.y - Size.y > SCREEN_HEIGHT * 2.0f)
	{
		// 破棄予約
		SetRelease();
	}
}